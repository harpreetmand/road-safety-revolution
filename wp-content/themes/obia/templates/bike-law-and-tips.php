        <div class="yellowBack condensedFont"><strong>It was a hit-and-run, can I still start a lawsuit?</strong></div>
        <p>Yes, you may be able to. The insurance regime in Ontario is set up to compensate victims of hit and runs, even if the driver remains unidentified. You may also be able to access SABS (Statutory Accident Benefits Schedule). </p>
        <div class="yellowBack condensedFont"><strong>I was “doored”, is that a traffic accident?</strong></div>
        <p>Yes. In Ontario, any incident arising from the normal use and operation of an automobile is covered by insurance. Depending on the circumstances, this may entitle you to a SABS (Statutory Accident Benefits Schedule) claim.</p>
        <div class="yellowBack condensedFont"><strong>I wasn’t wearing a helmet, can I still make a claim?</strong></div>
        <p>Yes. The law recognizes that nobody is perfect (including cyclists). Even if you have made a mistake, such as not wearing a helmet, or cycling through a stop sign, you may still be entitled to compensation.</p>
